package oct.ex041024_Constructor;

public class Person {
    // Attribute
    String name;
    int rollno;
    String hands;
    String gender;
    // Behavior / > Methods (function within the class)
    void walk() {
        System.out.println("I can walk.");
    }

    void talk() {
        System.out.println("I can talk.");
    }

    void sleep() {
        System.out.println("I can sleep.");
    }

    void read() {
        System.out.println("I can read.");
    }
}
