package sept.ex250924;

public class Lab077_Example7_LoopWithCondition {
    public static void main(String[] args) {
        // Loop with condition
        for (int i = 1; i <=10 ; i++) {
            if( i == 5){
                System.out.println("Five");
            }else{
                System.out.println(i);
            }
        }
        //Output :-
        // 1
        //2
        //3
        //4
        //Five
        //6
        //7
        //8
        //9
        //10
    }
}
