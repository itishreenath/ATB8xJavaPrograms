package sept.ex250924;

public class Lab080_WhileLoop {
    public static void main(String[] args) {
        // while - A, B, C
        // A - Init
        // B - Condition
        // C - Increment / Decrement

        int i = 0; // A
        while (i <= 10) {  // B
            System.out.println(i);
            i++; // C
        }
    }
}
