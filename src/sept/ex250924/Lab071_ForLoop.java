package sept.ex250924;

public class Lab071_ForLoop {
    public static void main(String[] args) {
         // Loop is used to perform a task which is repetitive in nature.
        // for( A ;B ; C)
        // A -> Initialization -> JVM What variable, where you are starting point
        // B - Condition -> When you want this to stop.
        // C ->  Increment / Decrement
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
