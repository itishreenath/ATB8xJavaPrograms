package sept.ex130924;

public class Lab018_Constants {
    public static void main(String[] args) {
        // Constant
        // constants are variables
        // whose values are fixed and cannot be changed once they are assigned.
        // final keyword is used.Syn- final dataType CONSTANT_NAME = CONSTANT_VALUE;
        final float PIE;
        PIE = 3.1456f;
        //PIE = 12.34f;
        System.out.println(PIE);
    }
}
